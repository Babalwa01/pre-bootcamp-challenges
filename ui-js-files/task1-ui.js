// declaration and initialization of x and y variables
var x = 0;
var y = 1;

// prints value of x and y variables to the console 
document.write("x = " + x + "<br>");
document.write("y = " + y + "<br>");

// modify value of x and y variables
x = x + 3;
y = y + x;

// prints new value of x and y variables to the console
document.write("x + 3 = " + x + "<br>");
document.write("y + x = " + y);